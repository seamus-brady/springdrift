verify_env_file() {
  local env_file="$1"
  local provider="$2"

  if [[ ! -f "$env_file" ]]; then
    return 1
  fi

  (
    source "$env_file"
    case "$provider" in
      anthropic)
        [[ -n "${ANTHROPIC_API_KEY:-}" ]]
        ;;
      mistral)
        [[ -n "${MISTRAL_API_KEY:-}" ]]
        ;;
      vertex)
        [[ -n "${VERTEX_PROJECT_ID:-}" ]]
        ;;
      local|mock)
        return 0
        ;;
      *)
        return 1
        ;;
    esac
  )
}

# ── Port check ───────────────────────────────────────────────────────────────

check_port_available() {
  local port="$1"

  if command -v lsof &>/dev/null; then
    if lsof -i ":${port}" -sTCP:LISTEN &>/dev/null; then
      return 1
    fi
    return 0
  elif command -v ss &>/dev/null; then
    if ss -tlnp 2>/dev/null | grep -q ":${port} "; then
      return 1
    fi
    return 0
  else
    return 0
  fi
}

# ── TOML recipients formatting ──────────────────────────────────────────────

format_recipients_toml() {
  local recipients="$1"
  if [[ -z "$recipients" ]]; then
    echo "[]"
  else
    echo "[$(echo "$recipients" | sed 's/[[:space:]]*,[[:space:]]*/", "/g; s/^/"/; s/$/"/' )]"
  fi
}
