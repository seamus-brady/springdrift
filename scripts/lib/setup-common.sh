# ── Env file generation ──────────────────────────────────────────────────────

generate_env_file() {
  local output_file="$1"
  local anthropic_key="${2:-}"
  local mistral_key="${3:-}"
  local vertex_project="${4:-}"
  local brave_key="${5:-}"
  local jina_key="${6:-}"
  local agentmail_key="${7:-}"
  local web_token="${8:-}"
  local script_name="${9:-setup.sh}"

  {
    echo "# Springdrift environment — generated by $script_name"
    echo "# Source this file before running: source $output_file && gleam run"
    echo ""
    [[ -n "$anthropic_key" ]] && echo "export ANTHROPIC_API_KEY=\"$anthropic_key\"" || true
    [[ -n "$mistral_key" ]] && echo "export MISTRAL_API_KEY=\"$mistral_key\"" || true
    [[ -n "$vertex_project" ]] && echo "export VERTEX_PROJECT_ID=\"$vertex_project\"" || true
    [[ -n "$brave_key" ]] && echo "export BRAVE_API_KEY=\"$brave_key\"" || true
    [[ -n "$jina_key" ]] && echo "export JINA_API_KEY=\"$jina_key\"" || true
    [[ -n "$agentmail_key" ]] && echo "export AGENTMAIL_API_KEY=\"$agentmail_key\"" || true
    [[ -n "$web_token" ]] && echo "export SPRINGDRIFT_WEB_TOKEN=\"$web_token\"" || true
  } > "$output_file"
}

