REQUIRED_GLEAM_MAJOR=1
REQUIRED_GLEAM_MINOR=11

# ── Colours (can be overridden before sourcing) ─────────────────────────────
GREEN="${GREEN:-\033[0;32m}"
YELLOW="${YELLOW:-\033[1;33m}"
RED="${RED:-\033[0;31m}"
BOLD="${BOLD:-\033[1m}"
NC="${NC:-\033[0m}"

ok()   { echo -e "  ${GREEN}✓${NC} $1"; }
warn() { echo -e "  ${YELLOW}!${NC} $1"; }
fail() { echo -e "  ${RED}✗${NC} $1"; exit 1; }

# ── Preflight ────────────────────────────────────────────────────────────────

check_repo_root() {
  if [[ ! -f "${1:-.}/gleam.toml" ]]; then
    return 1
  fi
  return 0
}

check_not_root() {
  [[ "$(id -u)" -ne 0 ]]
}

check_springdrift_writable() {
  local dir="${1:-.springdrift}"
  if [[ -d "$dir" ]]; then
    [[ -w "$dir" ]]
  else
    return 0
  fi
}

# ── Gleam version ────────────────────────────────────────────────────────────

parse_gleam_version() {
  echo "$1" | grep -oE '[0-9]+\.[0-9]+\.[0-9]+' | head -1
}

gleam_version_ok() {
  local ver="$1"
  if [[ -z "$ver" ]]; then
    return 1
  fi
  local major minor
  major=$(echo "$ver" | cut -d. -f1)
  minor=$(echo "$ver" | cut -d. -f2)
  if [[ "$major" -gt "$REQUIRED_GLEAM_MAJOR" ]] || \
     { [[ "$major" -eq "$REQUIRED_GLEAM_MAJOR" ]] && [[ "$minor" -ge "$REQUIRED_GLEAM_MINOR" ]]; }; then
    return 0
  fi
  return 1
}

